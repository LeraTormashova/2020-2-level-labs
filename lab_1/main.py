def tokenize(text):
    import re
    if type(text) == str:
        new_text = ''
        for elem in text:
            if elem.isalpha() or re.fullmatch('[\s]', elem):
                new_text += elem
        new_text = new_text.lower()
        new_text = new_text.split()
        return new_text
    else:
        return []

